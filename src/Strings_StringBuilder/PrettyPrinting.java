package Strings_StringBuilder;

public class PrettyPrinting {
    public static void main(String[] args) {


        float a = 432.789543f; //value can be rounded off
        System.out.printf("formatted number is %.2f", a); //%- placeholder
        System.out.println();

        System.out.println(Math.PI);
        System.out.printf("new: %.3f", Math.PI);

        System.out.println();
        System.out.printf("Hello my name is %s and I am %s", "Disha", "Cool");
    }

}
